#include "libft.h"

int	ft_atoi(const char *str)
{
	int	sign;
	int	b;
	int	i;

	i = 0;
	sign = 1;
	b = 0;
	while (str[i] != '\0' && (str[i] == '\t' || str[i] == '\n'
			|| str[i] == '\v' || str[i] == '\f' || str[i] == '\r'
			|| str[i] == ' '))
		i++;
	if (str[i] == '+')
		i++;
	else if (str[i] == '-')
	{
		sign = -1;
		i++;
	}
	while (str[i] != '\0' && str[i] >= '0' && str[i] <= '9')
	{
		b = b * 10 + (str[i] - 48);
		i++;
	}
	return (b * sign);
}
