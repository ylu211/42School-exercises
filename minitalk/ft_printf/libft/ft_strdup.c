#include"libft.h"

char	*ft_strdup(const char *s1)
{
	char	*a;
	int		b;

	b = 0;
	a = (char *)malloc(sizeof(char) * ft_strlen(s1)+1);
	if (!a)
		return (0);
	while (s1[b] != '\0')
	{
		a[b] = s1[b];
		b++;
	}
	a[b] = '\0';
	return (a);
}
