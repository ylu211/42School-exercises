#include "libft.h"

int	ifstr(char *c)
{
	if (c == NULL)
	{
		ft_putstr_fd("(null)", 1);
		return (6);
	}
	else
	{
		ft_putstr_fd(c, 1);
		return (ft_strlen(c));
	}
}
